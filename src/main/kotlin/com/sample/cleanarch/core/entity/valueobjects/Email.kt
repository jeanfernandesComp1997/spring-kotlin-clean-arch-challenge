package com.sample.cleanarch.core.entity.valueobjects

import java.util.regex.Pattern

class Email(
    val value: String
) {

    companion object {
        private const val INVALID_EMAIL_ERR = "This e-mail is invalid."
    }

    fun validate() {
        require(
            Pattern.compile(
                "^(([\\w-]+\\.)+[\\w-]+|([a-zA-Z]|[\\w-]{2,}))@"
                        + "((([0-1]?[0-9]{1,2}|25[0-5]|2[0-4][0-9])\\.([0-1]?"
                        + "[0-9]{1,2}|25[0-5]|2[0-4][0-9])\\."
                        + "([0-1]?[0-9]{1,2}|25[0-5]|2[0-4][0-9])\\.([0-1]?"
                        + "[0-9]{1,2}|25[0-5]|2[0-4][0-9]))|"
                        + "([a-zA-Z]+[\\w-]+\\.)+[a-zA-Z]{2,4})$"
            ).matcher(value).matches()
        ) { INVALID_EMAIL_ERR }
    }
}